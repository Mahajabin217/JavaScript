// Math built-in function,which will be used for maths:

const min = Math.min(22, 10, 5, 99, 40, 53);
console.log(min);
//Output: 5

const max = Math.max(22, 10, 5, 99, 40, 53);
console.log(max);
// Output: 99

console.log(Math.PI);
// Output: 3.141592653589793

console.log(Math.abs(5 - 10));
// Output: 5-10=-5 = 5

console.log(Math.round(2.76));
// Output: 3

console.log(Math.floor(5.13));
// Output: 5

console.log(Math.ceil(7.14));
// Output: 8

console.log(Math.random());
// Output: ans will be different different