// The push() method adds a new element to an array (at the end):

const color = ['red','blue','yellow','black','pink'];
color.push('white');
console.log(color);

// Output: [ 'red', 'blue', 'yellow', 'black', 'pink', 'white' ]

// The unshift() method adds a new element to an array (at the beginning):

color.unshift('orange');
console.log(color);

// Output:  ['orange', 'red', 'blue', 'yellow', 'black', 'pink', 'white' ]