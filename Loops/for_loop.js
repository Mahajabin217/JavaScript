// How to Use For Loops:

// How to print 1 to 5 using for loop:
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// How to find out Even or Odd Numbers using for loop:
// Even
for (let i = 0; i < 10; i = i + 2) {
    console.log(i);
}

// Odd
for (let i = 1; i < 10; i = i + 2) {
    console.log(i);
}